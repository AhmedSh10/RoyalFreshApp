@Composable
fun RoyalFreshTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    val colorScheme = if (darkTheme) {
        DarkColorScheme
    } else {
        LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        // You can also define typography and shapes here
        // typography = Typography,
        // shapes = Shapes,
        content = content
    )
}




